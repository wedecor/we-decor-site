function toAltText(filename) {
  return filename
    .replace(/[-_]/g, ' ')
    .replace(/\.[^/.]+$/, '')
    .replace(/\s+/g, ' ')
    .replace(/\b\w/g, c => c.toUpperCase());
}

async function fetchAllImages() {
  let nextCursor = undefined;
  const resources = [];
  do {
    const result = await cloudinary.search
      .expression('folder:we-decor/*')
      .max_results(500)
      .next_cursor(nextCursor)
      .execute();
    resources.push(...result.resources);
    nextCursor = result.next_cursor;
  } while (nextCursor);
  return resources;
}

(async () => {
  const images = await fetchAllImages();
  const galleryMap = {};
  
  images.forEach(img => {
    if (!img.asset_folder || !img.asset_folder.startsWith('we-decor/')) return;
    
    const folder = img.asset_folder.replace('we-decor/', '');
    const format = img.format?.toLowerCase();
    
    // Skip images that might be corrupted or too small
    if (img.width < 100 || img.height < 100) {
      console.log(`Skipping ${img.filename} - too small: ${img.width}x${img.height}`);
      return;
    }
    
    // For HEIC images, use a simpler transformation
    let imageUrl = img.secure_url;
    if (format === 'heic') {
      // Use a simpler transformation that just converts to JPG
      imageUrl = img.secure_url.replace('/upload/', '/upload/f_jpg/');
      console.log(`Converting HEIC: ${img.filename} -> JPG`);
    }
    
    if (!galleryMap[folder]) galleryMap[folder] = [];
    galleryMap[folder].push({
      src: imageUrl,
      alt: toAltText(img.filename || path.basename(img.secure_url)),
    });
  });
  
  // Log summary
  console.log('\nGallery Summary:');
  Object.entries(galleryMap).forEach(([folder, images]) => {
    console.log(`${folder}: ${images.length} images`);
  });
  
  const fileContent =
    '// AUTO-GENERATED BY fix-gallery-urls.js\n' +
    'const gallery = ' + JSON.stringify(galleryMap, null, 2) + ';\n\nexport default gallery;\n';
  
  fs.writeFileSync(outputPath, fileContent);
  console.log('\nâœ… Fixed gallery mapping generated at utils/gallery.ts');
})(); 